package com.infosys.basic.dto;

public class ProcessCount {

	private long demanderRegisterCount;
	private long providerRegisterCount;
	private long newDemanderCount;
	private long processDemanderCount;

	public long getDemanderRegisterCount() {
		return demanderRegisterCount;
	}

	public void setDemanderRegisterCount(long demanderRegisterCount) {
		this.demanderRegisterCount = demanderRegisterCount;
	}

	public long getProviderRegisterCount() {
		return providerRegisterCount;
	}

	public void setProviderRegisterCount(long providerRegisterCount) {
		this.providerRegisterCount = providerRegisterCount;
	}

	public long getNewDemanderCount() {
		return newDemanderCount;
	}

	public void setNewDemanderCount(long newDemanderCount) {
		this.newDemanderCount = newDemanderCount;
	}

	public long getProcessDemanderCount() {
		return processDemanderCount;
	}

	public void setProcessDemanderCount(long processDemanderCount) {
		this.processDemanderCount = processDemanderCount;
	}

}
